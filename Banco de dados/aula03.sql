CREATE TABLE CIDADES(
  CODIGOCIDADE INT AUTO_INCREMENT PRIMARY KEY,
  NOMECIDADE VARCHAR (40),
  CODIGOESTADO INT,
  FOREIGN KEY (CODIGOESTADO) REFERENCES ESTADOS(CODIGOESTADO)
