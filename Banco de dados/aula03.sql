CREATE  VIEW SELECIONAR AS
SELECT
  PESSOAS.CODIGOPESSOA,
  PESSOAS.NOMEPESSOA,
  ESTADOS.NOMEESTADO,
  CIDADES.NOMECIDADE
FROM PESSOAS
INNER JOIN ESTADOS 
ON ESTADOS.CODIGOESTADO = PESSOAS.CODIGOESTADO
INNER JOIN CIDADES
ON CIDADES.CODIGOCIDADE = PESSOAS.CODIGOCIDADE;

SELECT * FROM SELECIONAR;