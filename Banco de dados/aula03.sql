CREATE TABLE PESSOAS (
  CODIGOPESSOA INT AUTO_INCREMENT PRIMARY KEY,
  NOMEPESSOA VARCHAR (40),
  CODIGOESTADO INT,
  CODIGOCIDADE INT,
  FOREIGN KEY (CODIGOESTADO) REFERENCES ESTADOS (CODIGOESTADO),
  FOREIGN KEY (CODIGOCIDADE) REFERENCES CIDADES (CODIGOCIDADE)
);


